function Mostrar()
{
	alert('iteración while');

	var contador;
	// siempre tiene que empezar en 0
	contador= 10;
	//while menor a las vueltas que quieren dar
	while	(contador>0)

	{

		document.write("<br> "+contador);
		contador --;
		//contador ++;
	}

	document("<br>sali "+ contador);


}//FIN DE LA FUNCIÓN